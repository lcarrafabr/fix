    public void getGradeSituacao() throws ParseException{
       //Consultar pedidos
        Contatos oContatos = new Contatos();

        if ( nomejTextField.getText().length() > 0 ){
            oContatos.setNome(nomejTextField.getText()+"%");
        }

        ResultSet resultSet = null;

        String[] colunasTabela = new String[]{"CÃ³digo", "Nome", "Tel"};
        DefaultTableModel modeloTabela = new DefaultTableModel(null, colunasTabela) {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };
        gradeContatosjTable.setModel(modeloTabela);
        gradeContatosjTable.getColumnModel().getColumn(0).setPreferredWidth(10);
        gradeContatosjTable.getColumnModel().getColumn(1).setPreferredWidth(250);
        gradeContatosjTable.getColumnModel().getColumn(2).setPreferredWidth(100);

try {
            resultSet = oContatos.getConsultar(conexao);
            if (resultSet.getRow() == 1){
             resultSet.first();
            }


        } catch (SQLException ex) {
            Logger.getLogger(contato.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {

            while (resultSet.next()) {

                modeloTabela.addRow(new String[]{
                    Funcoes.getCodigoFormat(resultSet.getInt("cod")),
                    resultSet.getString("nome"),
                    resultSet.getString("tel")
                });

            }

        } catch (SQLException ex) {
            Logger.getLogger(contato.class.getName()).log(Level.SEVERE, null, ex);
        }

           }

    public void getFieldContatos(int codContatos) throws ParseException{
        //Buscar contato
        Contatos oContatos = new Contatos();
        ResultSet rs = null;
        oContatos.setCod(codContatos);

        try {
            rs = oContatos.getConsultar(conexao);
            rs.next();

            //Pegar campos
            nomejTextField.setText(rs.getString("nome"));
            teljFormattedTextField.setText(rs.getString("tel"));
            observacaojTextArea.setText(rs.getString("observacao"));

        } catch (SQLException ex) {
            Logger.getLogger(contato.class.getName()).log(Level.SEVERE, null, ex);
        }


    }




